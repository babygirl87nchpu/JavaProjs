package edu.vt.cs5044;

/**
 * Indicates which direction is activated, for any button, handle, or lever with two directions.
 * 
 * IMPORTANT: Please DO NOT modify this file in any way!
 *
 * @author M. El-Arnaoty
 * @version Spring 2023
 */
public enum Direction {
    
    /**
     * Indicates the button or lever is activated to the direction that will open the door.
     */
    OPEN,
    
    /**
     * Indicates the button or lever is activated to the direction that will close the door.
     */
    CLOSE;

}
